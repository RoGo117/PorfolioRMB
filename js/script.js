/* Abrir terminal: Ctrl + ñ
   (Se necesita tener una cuenta de GitHub y tener git descargado en el equipo)
   
 - git init

 - git add index.html                      (para añadir el fichero concreto index.html)
 - git add *.css                           (para añadir los ficheros .css)
 - git add .                               (para añadir todo)

 - git status -s                           (para saber en qué estado están los ficheros)

 - git commit -m "Initial commit"          (para documentar los cambios con el nombre "Initial commit")
 - git log                                 (para ver el registro de los commits, muestra la versión también)
 - git log --oneline                       (igual al git log, pero escrito todo en la misma línea)

 - git reset --hard dec1d9996fc...         (para cargar a una versión anterior)
 - git reflog                              (como un log, pero también muestra los commits borrados o cambiados)

*/

